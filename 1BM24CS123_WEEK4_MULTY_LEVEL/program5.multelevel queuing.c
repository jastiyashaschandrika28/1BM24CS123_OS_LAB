#include<stdio.h>
struct process
{
    int pid;
    int at;
    int bt,ct,tat,wt;
    int type;
};
int main()
{
    struct process p[100];
    int i,time=0,n, systemq[100],userq[100],s=0,u=0;
    printf("Enter the number od process:");
    scanf("%d",&n);
    printf("Enter the arrival time burst time and type of all the process respectively:");
    for(i=0;i<n;i++)
    {
        p[i].pid=i;
        scanf("%d %d %d",&p[i].at,&p[i].bt,&p[i].type);
    }

    for(i=0;i<n;i++)
    {
        if(p[i].type==0)
        {
            systemq[s]=p[i].pid;
            s++;

        }
        else
        {
            userq[u]=p[i].pid;
            u++;

        }
    }

    for(i=0;i<s-1;i++)
    {

        for(int j=0;j<s-i-1;j++)
        {
            if(p[systemq[j]].at>p[systemq[j+1]].at)
            {
                int temp=systemq[j+1];
                systemq[j+1]=systemq[j];
                systemq[j]=temp;
            }
        }
    }

     for(i=0;i<u-1;i++)
    {

        for(int j=0;j<u-i-1;j++)
        {
            if(p[userq[j]].at>p[userq[j+1]].at)
            {
                int temp=userq[j+1];
                userq[j+1]=userq[j];
                userq[j]=temp;
            }
        }
    }

  for(i=0;i<s;i++)
  {
      if(time<p[systemq[i]].at)
        time=p[systemq[i]].at;
     time=time+p[systemq[i]].bt;
     p[systemq[i]].ct=time;
     p[systemq[i]].tat=p[systemq[i]].ct-p[systemq[i]].at;
     p[systemq[i]].wt=p[systemq[i]].tat-p[systemq[i]].bt;
  }
for (i = 0; i < u; i++) {
    if (time < p[userq[i]].at)
        time = p[userq[i]].at;
    time = time + p[userq[i]].bt;
    p[userq[i]].ct = time;
    p[userq[i]].tat = p[userq[i]].ct - p[userq[i]].at;
    p[userq[i]].wt = p[userq[i]].tat - p[userq[i]].bt;
}
  printf("pid \t at \t bt \t ct \t tat \t wt \t type \n");
  for(i=0;i<n;i++)
  {
      printf("%d \t %d \t %d \t %d \t %d \t %d \t %d \n",p[i].pid,p[i].at,p[i].bt,p[i].ct,p[i].tat,p[i].wt,p[i].type);
  }

}























































































































































































































































































































































































































































































































































































































































































































































































































































































































